/** 列出所有可用预设 id */
export function listPresetIds(): string[] {
  return Object.keys(PRESET_RULES);
}

/** 获取预设规则（含 title + content） */
export function getPresetRule(presetId: string): { title: string; content: string } | null {
  return PRESET_RULES[presetId] ?? null;
}

/* ─────────── 开发期完整性校验 ─────────────────────────────────────────
 * 防止以后加新 preset 到客户端 PROMPT_PRESETS 数组却忘记在本文件加 rule
 * —— 那样会**静默**走 chatSystemPromptServer 的兜底（拼客户端 desc+tags），
 * 没人会发现规则没写、AI 体验不如预期。
 *
 * 这里在模块首次 import 时校验：客户端每个 preset id 都必须在 PRESET_RULES
 * 里能找到对应 rule。漏了就抛错（dev/build 启动立刻挂掉），不让漏网。
 * 生产环境跳过校验以避免冷启动额外开销（dev/build 已经覆盖）。
 * ─────────────────────────────────────────────────────────────────── */
if (process.env.NODE_ENV !== "production") {
  // 异步 import 避免循环依赖（promptPresets 是客户端文件，promptPresetsServer 仅服务端）
  void import("./promptPresets").then(({ PROMPT_PRESETS }) => {
    const missing = PROMPT_PRESETS
      .map((p) => p.id)
      .filter((id) => !PRESET_RULES[id]);
    if (missing.length > 0) {
      throw new Error(
        `[promptPresetsServer] 客户端 PROMPT_PRESETS 里有 ${missing.length} 个 preset 没在服务端注册 rule:\n` +
          missing.map((id) => `  - ${id}`).join("\n") +
          `\n请在 src/lib/r2v/promptPresetsServer.ts 的 PRESET_RULES 里补完 rule，` +
          `不然这些 preset 选了会走通用兜底（精细度不如手写 rule）。`,
      );
    }
  });
}
